

function entrou() {
    let div = document.querySelector('div')
    div.innerHTML = 'Entrou!'
    div.style.backgroundColor = 'red'
}

function saiu() {
    let div = document.querySelector('div')
    div.innerHTML = 'Saiu!'
    div.style.background = 'yellow'
}

function clicou() {
    let div = document.querySelector('div')
    div.innerHTML = 'Clicou!'
    div.style.background = 'blue'
}












